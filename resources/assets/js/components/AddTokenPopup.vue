<script>

    import modal from './modal.vue';
    export default {
        name: 'app',
        components: {
            modal,
        },
        data () {
            return {
                isModalVisible: false,
            };
        },
        methods: {
            showModal() {
                this.isModalVisible = true;
            },
            closeModal() {
                this.isModalVisible = false;
            }
        },
    };
</script>

<template>
  <div id="app">
    <button
            type="button"
            class="btn btn-primary"
            @click="showModal"
    >
      Add new token
    </button>

    <modal
            v-show="isModalVisible"
            @close="closeModal"
    />
  </div>
</template>